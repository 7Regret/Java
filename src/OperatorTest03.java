
public class OperatorTest03 {

	public static void main(String[] args) {
		int score1 = 10;
		int score2 = 3;
		
		System.out.println("+ 연산 결과: " + (score1 + 3));	// 더하기
		System.out.println("- 연산 결과: " + (score1 - 3));	// 빼기
		System.out.println("* 연산 결과: " + (score1 * 3));	// 곱하기
		System.out.println("/ 연산 결과: " + (score1 / 3));	// 나누기
		System.out.println("% 연산 결과: " + (score1 % 3));	// 나머지
	}

}
